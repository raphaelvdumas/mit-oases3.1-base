      COMPLEX RHS(NLA3)    
      COMMON /EQVAR/ NEQ,IPS(NLA,4),ICP(NLA2),IRN(NLA1),IDP(NLA3),
     &               NNA,ISTART(NLA),IRST(NLA),ICST(NLA),NCL(NLA),
     &               NRI(NLA),RHS,IBW,EPS,IRHCOL,NNB
