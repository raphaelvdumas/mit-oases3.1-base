      COMMON /TISO/ NTISOL,NSL(NLA),ASP,BSP,ARO(3,NLA),
     &              AH(3,NLA),LAYNTI(NLA) 
      COMPLEX ANSTD(26,NLA)
      COMMON /ANSTD/ ANSTD
